    private void btCommitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCommitActionPerformed
        if (tpDescription.getText().length() > 600) {
            JOptionPane.showMessageDialog(null, "description is too long", "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            rm = new RequestManager();
            character = new PlayerCharacter(
                    cbLevel.getSelectedIndex() + 1,
                    Session.getCurrentUserId(),
                    tfCharacterName.getText().trim(),
                    cbGender.getSelectedItem().toString(),
                    tfClass.getText().trim(),
                    tfRace.getText().trim(),
                    tpDescription.getText().trim());
            if (rm.createCharacter(character)) {
                JOptionPane.showMessageDialog(null, "Character '" + character.getName()
                        + "' succesfully created!", "Success!",
                        JOptionPane.OK_OPTION);
                cl.show(characterContainer, "selectedCharacter");
            }
        }
    }//GEN-LAST:event_btCommitActionPerformed

