    public void setCharacter(PlayerCharacter character) {
        this.character = character;
        tfCharacterName.setText(character.getName());
        tfClass.setText(character.getClassType());
        tfRace.setText(character.getRace());
        cbGender.setSelectedItem(character.getGender());
        cbLevel.setSelectedIndex(character.getLevel() - 1);
        tpDescription.setText(character.getDescription());
        countChars();
    }

    public void InitiateLevelBox() {
        for (int i = 1; i <= 30; i++) {
            cbLevel.addItem(i);
        }
    }

    public void countChars() {
        if (tpDescription.getText().length() == 0) {
            lblWordCount.setText(EMPTY);
        }
        if (tpDescription.getText().length() > 600) {
            lblWordCount.setText("<html><font color='red'>"
                    + tpDescription.getText().length()
                    + " / 600 max. characters </font></html>");
        } else {
            lblWordCount.setText(tpDescription.getText().length() + " / 600 max. characters");
        }
    }

